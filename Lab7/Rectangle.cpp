#include <iostream>
using namespace std;
#include "Rectangle.h"

// ======= Default constructor =======================================
Rectangle::Rectangle()
{
   double width = 0;
   double length = 0;
}
//====================================================================

//===== Deconstructor ================================================
Rectangle::~Rectangle() {}
//====================================================================

//======= setWidht method ============================================
void Rectangle::setWidth(double width)
{
    this->width = width;
}
//====================================================================

// ====== setLength method ===========================================
void Rectangle::setLength(double length)
{
    this->length = length;
}
//====================================================================

// ====== getWidth method ============================================
double Rectangle::getWidth()const
{
       return width;
}
//====================================================================

// ====== getLength method ===========================================
double Rectangle::getLength()const
{
    return length;
}
//====================================================================

// ====== getArea method =============================================
double Rectangle::getArea()const
{
    return width * length;
}
